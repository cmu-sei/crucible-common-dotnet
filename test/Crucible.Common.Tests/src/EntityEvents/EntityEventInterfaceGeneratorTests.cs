    }

    [Fact]
    public void Generator_OutputsAutoGeneratedComment()
    {
        var source = """
            using System;

            namespace Crucible.Common.EntityEvents
            {
                [AttributeUsage(AttributeTargets.Class, AllowMultiple = false)]
                public class GenerateEntityEventInterfacesAttribute : Attribute
                {
                    public Type[] InterfaceTypes { get; }
                    public GenerateEntityEventInterfacesAttribute(params Type[] interfaceTypes)
                    {
                        InterfaceTypes = interfaceTypes;
                    }
                }
            }

            public interface ITest { }

            [Crucible.Common.EntityEvents.GenerateEntityEventInterfaces(typeof(ITest))]
            public class TestDbContext { }
            """;

        var (generatedCode, diagnostics) = RunGenerator(source);

        Assert.Empty(diagnostics.Where(d => d.Severity == DiagnosticSeverity.Error));
        Assert.NotNull(generatedCode);
        Assert.Contains("// <auto-generated/>", generatedCode);
        Assert.Contains("#nullable enable", generatedCode);
        Assert.Contains("namespace Crucible.Common.EntityEvents.Events", generatedCode);
    }

    private static (string? GeneratedCode, ImmutableArray<Diagnostic> Diagnostics) RunGenerator(string source)
    {
        var syntaxTree = CSharpSyntaxTree.ParseText(source);

        var references = AppDomain.CurrentDomain.GetAssemblies()
            .Where(a => !a.IsDynamic && !string.IsNullOrEmpty(a.Location))
            .Select(a => MetadataReference.CreateFromFile(a.Location))
            .Cast<MetadataReference>()
            .ToList();

        var compilation = CSharpCompilation.Create(
            "TestAssembly",
            [syntaxTree],
            references,
            new CSharpCompilationOptions(OutputKind.DynamicallyLinkedLibrary));

        var generator = new EntityEventInterfaceGenerator();

        GeneratorDriver driver = CSharpGeneratorDriver.Create(generator);
        driver = driver.RunGeneratorsAndUpdateCompilation(compilation, out var outputCompilation, out var generatorDiagnostics);

        var results = driver.GetRunResult();
        var generatedSource = results.GeneratedTrees.FirstOrDefault();

        // Combine compilation and generator diagnostics
        var allDiagnostics = outputCompilation.GetDiagnostics().AddRange(generatorDiagnostics);

        return (generatedSource?.GetText().ToString(), allDiagnostics);
    }
}
