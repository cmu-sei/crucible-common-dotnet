using System.Collections.Immutable;
using Crucible.Common.EntityEvents.Generators;
using Microsoft.CodeAnalysis;
using Microsoft.CodeAnalysis.CSharp;

namespace Crucible.Common.Tests.EntityEvents;

public class EntityEventInterfaceGeneratorTests
{
    [Fact]
    public void Generator_WithSingleAttribute_GeneratesPartialClasses()
    {
        var source = """
            using System;

            namespace Crucible.Common.EntityEvents
            {
                [AttributeUsage(AttributeTargets.Class, AllowMultiple = false)]
                public class GenerateEntityEventInterfacesAttribute : Attribute
                {
                    public Type[] InterfaceTypes { get; }
                    public GenerateEntityEventInterfacesAttribute(params Type[] interfaceTypes)
                    {
                        InterfaceTypes = interfaceTypes;
                    }
                }
            }

            public interface ITestNotification { }

            [Crucible.Common.EntityEvents.GenerateEntityEventInterfaces(typeof(ITestNotification))]
            public class TestDbContext { }
            """;

        var (generatedCode, diagnostics) = RunGenerator(source);

        // Report any compilation diagnostics for debugging
        Assert.Empty(diagnostics.Where(d => d.Severity == DiagnosticSeverity.Error));

        Assert.NotNull(generatedCode);
        Assert.Contains("public partial class EntityCreated<TEntity> : global::ITestNotification", generatedCode);
        Assert.Contains("public partial class EntityUpdated<TEntity> : global::ITestNotification", generatedCode);
        Assert.Contains("public partial class EntityDeleted<TEntity> : global::ITestNotification", generatedCode);
    }

    [Fact]
    public void Generator_WithMultipleTypes_GeneratesMultipleInterfaces()
    {
        var source = """
            using System;

            namespace Crucible.Common.EntityEvents
            {
                [AttributeUsage(AttributeTargets.Class, AllowMultiple = false)]
                public class GenerateEntityEventInterfacesAttribute : Attribute
                {
                    public Type[] InterfaceTypes { get; }
                    public GenerateEntityEventInterfacesAttribute(params Type[] interfaceTypes)
                    {
                        InterfaceTypes = interfaceTypes;
                    }
                }
            }

            public interface INotification1 { }
            public interface INotification2 { }

            [Crucible.Common.EntityEvents.GenerateEntityEventInterfaces(typeof(INotification1), typeof(INotification2))]
            public class TestDbContext { }
            """;

        var (generatedCode, diagnostics) = RunGenerator(source);

        Assert.Empty(diagnostics.Where(d => d.Severity == DiagnosticSeverity.Error));
        Assert.NotNull(generatedCode);
        Assert.Contains("global::INotification1", generatedCode);
        Assert.Contains("global::INotification2", generatedCode);
    }

    [Fact]
    public void Generator_WithNoAttribute_GeneratesNoCode()
    {
        var source = """
            public class TestClass { }
            """;

        var (generatedCode, _) = RunGenerator(source);

        Assert.Null(generatedCode);
    }

    [Fact]
    public void Generator_WithNamespacedInterface_UsesFullyQualifiedName()
